package com.infosys;

public class CalculatorUtil {
    int add(int a, int b)
    {
        return a+b;
    }
    int mul(int a, int b)
    {
        return a*b;
    }
    int div(int a, int b)
    {
        return a/b;
    }
    public String  parseString(String n)
    {
        return n;
    }
    public boolean isEven(int n){
        return n%2==0;
    }


}
